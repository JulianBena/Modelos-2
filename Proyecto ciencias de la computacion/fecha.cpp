// estructura para el manejo de las fechas
struct Fecha {
  int fecha_seg;   // segundos del minuto desde 0 hasta 60
  int fecha_min;   // minutos de la hora desde 0 hasta 59
  int fecha_hora;  // horas del dia desde 0 hasta 24
  int fecha_mdia;  // dia del mes desde 1 hasta 31
  int fecha_mes;   // mes del año desde 0 hasta 11
  int fecha_anio;  // año
};
